my_tuple = (1, 2, 3, 4, 5, 6, 7)

print(my_tuple[1:4])
# Output: (2, 3, 4)

print(my_tuple[4:])
# Output: (5, 6, 7)

print(my_tuple[-4:])
# Output: (4, 5, 6, 7)
