# LOOP over elements in a LIST
myList = [1, 2, 3, 4, 5]
for element in myList:
    print(element)  
    # Prints all the elements in new line
# Output: 1
#         2
#         3
#         4
#         5

# Loop through a subset of list of elements
print("Sublist :")
for element in myList[2:4]:
    print(element)  
    # Prints all the elements in new line
# Output: Sublist :
#         3
#         4