adj = ["red", "big", "tasty"]
fruits = ["apple", "banana", "cherry"]

for ad in adj:
    for fruit in fruits:
        print(ad, fruit)
# OUTPUT: red apple
#         red banana
#         red cherry
#         big apple
#         big banana
#         big cherry
#         tasty apple
#         tasty banana
#         tasty cherry
