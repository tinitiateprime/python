list1 = [1, 2, 3]
list2 = [4, 5, 6]

# Concatenation
print(list1 + list2)   # Output: [1, 2, 3, 4, 5, 6]

# Repetition
print(list1 * 3)       # Output: [1, 2, 3, 1, 2, 3, 1, 2, 3]

# Membership test
print(2 in list1)      # Output: True

# Comparision
print(list1 == list2)  # Output: False