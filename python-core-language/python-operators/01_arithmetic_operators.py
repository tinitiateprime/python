a = 10
b = 3
# Addition
print(a + b)  # Output: 13

# Subtraction
print(a - b)  # Output: 7

# Multiplication
print(a * b)  # Output: 30

# Division
print(a / b)  # Output: 3.3333333333333335

# Floor Division
print(a // b) # Output: 3

# Modulus
print(a % b)  # Output: 1

# Power/Exponentiation
print(a ** b) # Output: 1000