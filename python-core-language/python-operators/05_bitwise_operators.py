a = 10
b = 4

# Bitwise AND
print(a & b)   # Output: 0

# Bitwise OR
print(a | b)   # Output: 14

# Bitwise XOR
print(a ^ b)   # Output: 14

# Bitwise NOT
print(~a)      # Output: -11

# Left shift
print(a << 1)  # Output: 20

# Right shift
print(a >> 1)  # Output: 5