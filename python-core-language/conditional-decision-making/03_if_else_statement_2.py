A = 100
B = 100
C = 200

# Check if A = B, and print a message with if condition
if A == B:
    print('A and B are same !') 
# OUTPUT: A and B are same !

# Print with else condition
if B == C:
    print('B and C are same !')
else:
    print('B and C are NOT same !')
# OUTPUT: B and C are NOT same !
