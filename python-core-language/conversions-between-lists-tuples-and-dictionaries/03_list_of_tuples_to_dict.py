list_of_tuples = [('name', 'John'), ('age', 30), ('gender', 'Male')]
print(list_of_tuples)
# OUTPUT: [('name', 'John'), ('age', 30), ('gender', 'Male')]

my_dict = dict(list_of_tuples)
print(my_dict)  
# OUTPUT: {'name': 'John', 'age': 30, 'gender': 'Male'}
